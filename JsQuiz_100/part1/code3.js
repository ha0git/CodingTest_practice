/* 문제3: 변수의 타입

다음 출력 값으로 올바른 것은?

```jsx
var arr = [100, 200, 300];
console.log(typeof(arr));
```

1)  undefined
2)  string
3)  number
4)  object

*/


var arr = [100, 200, 300];

console.log(typeof (arr));   //typeof : 자료형을 출력해준다.

//답 : 4번
//undefined, string, number 는 모두 기본 자료형(primitive type) 입니다.